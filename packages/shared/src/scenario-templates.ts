/**
 * 场景行业模板（前后端共享）
 *
 * 面向「场景配置」新建/编辑页顶部的「行业模板」选择：运营人员选中某个行业模板后，
 * 前端把模板预设内容整体回显进当前场景草稿（覆盖身份/沟通风格/业务目标/系统提示词/
 * 回复边界/开场白等字段），仍可在此基础上继续调整，不会自动保存。
 */
export interface ScenarioIndustryTemplate {
  /** 模板标识 */
  key: string;
  /** 模板名称，展示在「行业模板」选择标签上 */
  name: string;
  /** 模板说明（用于标签的 title 提示） */
  description: string;
  /** 机器人身份 */
  agentIdentity: string;
  /** 沟通风格，顿号分隔，便于拆分回显为多选标签（如「亲切、耐心」） */
  communicationStyle: string;
  /** 业务目标 */
  businessGoal: string;
  /** Agent 系统提示词（人设/知识边界/行为底线） */
  systemPrompt: string;
  /** 对 LLM 生成的约束清单（回复边界） */
  llmConstraints: string[];
  /** 问候语（通话接通后第一句话），支持 {变量} 占位符 */
  greeting: string;
}

